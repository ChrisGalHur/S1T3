package S1T3N1Ex1;

public class Month {
	
	//Atributos
	String name;

	public Month(String name) {
		this.name = name;
	}

	//Getters
	public String getName() {
		return name;
	}

	//Setters
	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return name;
	}
	
	

}
